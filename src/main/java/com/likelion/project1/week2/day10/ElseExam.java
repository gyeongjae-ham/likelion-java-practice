package com.likelion.project1.week2.day10;

public class ElseExam {
    public static void main(String[] args) {
        boolean checkTrue = true;
        if (checkTrue) {
            System.out.println("true 입니다");
        } else {
            System.out.println("false 입니다");
        }

        checkTrue = false;
        if (checkTrue) {
            System.out.println("true 입니다");
        } else {
            System.out.println("false 입니다");
        }
    }
}
